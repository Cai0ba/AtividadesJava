public class nomes {
    public static void main(String[] args) {
        String nome, sobreNome, nomeCompleto;
        nome = "Anna";
        sobreNome = "Lívia";
        nomeCompleto = nome + ' ' + sobreNome;
        System.out.println( nomeCompleto );   // Anna Lívia
        nomeCompleto = "Anna" + ' ' + "Lívia";
        System.out.println( nomeCompleto );   // Anna Lívia
    }
}





